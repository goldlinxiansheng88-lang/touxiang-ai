def _read_pool_file() -> List[str]:
    if not _POOL_FILE.is_file():
        return []
    return [ln.strip() for ln in _POOL_FILE.read_text(encoding="utf-8").splitlines() if ln.strip()]


def _dedupe_preserve(parts: Iterable[str]) -> List[str]:
    seen: Set[str] = set()
    out: List[str] = []
    for p in parts:
        if p in seen:
            continue
        seen.add(p)
        out.append(p)
    return out


def build_master_slugs() -> List[str]:
    """至少 210 条唯一 slug；不足则继续吃 _unsplash_id_pool 剩余。"""
    ordered: List[str] = []
    ordered.extend(
        _WEDDING_COUPLE
        + _COSPLAY_COSTUME
        + _FITNESS
        + _JOY
        + _CINEMATIC
        + _RETRO
        + _STYLEVISUALS_EXTRA
        + _DREAMY
        + _EXTRA_PORTRAITS
        + _FINAL_FILL
    )
    merged = _dedupe_preserve(ordered)
    pool_rest = [x for x in _read_pool_file() if x not in set(merged)]
    merged.extend(pool_rest)
    merged = _dedupe_preserve(merged)
    if len(merged) < 210:
        raise RuntimeError(f"theme pack photo pool too small: {len(merged)} < 210")
    return merged[:210]


