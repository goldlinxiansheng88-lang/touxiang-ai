def slugs_by_pack() -> Dict[str, List[str]]:
    master = build_master_slugs()
    out: Dict[str, List[str]] = {}
    i = 0
    for code in PACK_CODES_IN_ORDER:
        out[code] = master[i : i + 15]
        i += 15
    return out
